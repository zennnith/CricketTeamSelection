public class IplAllRounder extends Player {
    
    private String teamID;
    private String average;
    private String strRate;
    private String bowAvg;
    private String bowEcon;

    /**
     * @return the teamID
     */
    public String getTeamID() {
        return teamID;
    }

    /**
     * @param teamID the teamID to set
     */
    public void setTeamID(String teamID) {
        this.teamID = teamID;
    }

    /**
     * @return the average
     */
    public String getAverage() {
        return average;
    }

    /**
     * @param average the average to set
     */
    public void setAverage(String average) {
        this.average = average;
    }

    /**
     * @return the strRate
     */
    public String getStrRate() {
        return strRate;
    }

    /**
     * @param strRate the strRate to set
     */
    public void setStrRate(String strRate) {
        this.strRate = strRate;
    }

    /**
     * @return the bowAvg
     */
    public String getBowAvg() {
        return bowAvg;
    }

    /**
     * @param bowAvg the bowAvg to set
     */
    public void setBowAvg(String bowAvg) {
        this.bowAvg = bowAvg;
    }

    /**
     * @return the bowEcon
     */
    public String getBowEcon() {
        return bowEcon;
    }

    /**
     * @param bowEcon the bowEcon to set
     */
    public void setBowEcon(String bowEcon) {
        this.bowEcon = bowEcon;
    }
}
